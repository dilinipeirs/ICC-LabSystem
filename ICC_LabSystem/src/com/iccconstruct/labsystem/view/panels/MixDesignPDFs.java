    private void loadMixDesigns() throws Exception {
        ArrayList<String> all = concreteController.getPDFs();
        DefaultTableModel dtm = (DefaultTableModel) tblConcrete.getModel();
        dtm.setRowCount(0);
        for (String all1 : all) {
            Object[] rowData = {all1};
            dtm.addRow(rowData);
        }

    }

